public class switchCase {
        public static void main(String[] args) {

            int opcao = 2;

            switch (opcao) {
                case 1:
                    System.out.println("Caso 1");
                    break;

                case 2:
                    System.out.println("Caso 2");
                    break;

                case 3:
                    System.out.println("Caso 3");
                    break;

                default:
                    System.out.println("Caso padrão");
                    break;
            }
        }

        // if else usa para oreção booleana complexa, como quando se tem 3 entradas

    // switch e case para menus, etc

}
